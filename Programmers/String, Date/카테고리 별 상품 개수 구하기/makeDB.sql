-- 1. 테이블 초기화 (항상 맨 앞에 추가)
DROP TABLE IF EXISTS PRODUCT;

-- 2. 상품 정보 테이블 생성 (PK 생략, 모두 NOT NULL)
CREATE TABLE PRODUCT (
    PRODUCT_ID INTEGER NOT NULL,
    PRODUCT_CODE VARCHAR(8) NOT NULL,
    PRICE INTEGER NOT NULL
);

