-- 1. 테이블 초기화 (항상 맨 앞에 추가)
DROP TABLE IF EXISTS FISH_NAME_INFO;
DROP TABLE IF EXISTS FISH_INFO;

-- 2. 물고기 정보 테이블 생성 (PK 생략, LENGTH만 Null 허용)
CREATE TABLE FISH_INFO (
    ID INT NOT NULL,
    FISH_TYPE INT NOT NULL,
    LENGTH FLOAT,
    TIME DATE NOT NULL
);

-- 3. 물고기 이름 정보 테이블 생성 (PK 생략, 모두 NOT NULL)
CREATE TABLE FISH_NAME_INFO (
    FISH_TYPE INT NOT NULL,
    FISH_NAME VARCHAR(50) NOT NULL
);

-- 4. 데이터 삽입 (FISH_INFO)
